using System.Collections.Generic;
using System.Text;
using Stella.Ergosfare.SourceGenerator.Models;

namespace Stella.Ergosfare.SourceGenerator;

/// <summary>
///     Renders the generated registration source. The emitted code is deliberately
///     conservative C# (block namespace, no target-typed constructs) because it compiles
///     inside the consumer's project under whatever language version they use; every
///     registration is a direct <c>typeof(...)</c> argument so the trimmer/AOT analyzers
///     can statically satisfy the <c>DynamicallyAccessedMembers</c> annotations on the
///     registration APIs — the whole point of replacing assembly scanning.
/// </summary>
internal static class RegistrationEmitter
{
    private const string RegistryFullName = "global::Stella.Ergosfare.Core.Abstractions.Registry.IMessageRegistry";
    private const string CommandBuilderFullName = "global::Stella.Ergosfare.Commands.Extensions.MicrosoftDependencyInjection.CommandModuleBuilder";
    private const string QueryBuilderFullName = "global::Stella.Ergosfare.Queries.Extensions.MicrosoftDependencyInjection.QueryModuleBuilder";
    private const string EventBuilderFullName = "global::Stella.Ergosfare.Events.Extensions.MicrosoftDependencyInjection.EventModuleBuilder";

    /// <summary>
    ///     Emits the <c>ErgosfareGeneratedRegistrations</c> class for the given type lists.
    /// </summary>
    public static string Emit(
        IReadOnlyList<string> allTypes,
        IReadOnlyList<string> commandTypes,
        IReadOnlyList<string> queryTypes,
        IReadOnlyList<string> eventTypes,
        ModuleBuilderAvailability builders,
        string generatorVersion)
    {
        var sb = new StringBuilder(4096);

        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("// Generated by Stella.Ergosfare.SourceGenerator — do not edit.");
        sb.AppendLine("// Compile-time replacement for reflection-based assembly scanning (RegisterFromAssembly).");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("namespace Stella.Ergosfare.Generated");
        sb.AppendLine("{");
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    ///     Ergosfare constructs (messages, handlers, interceptors) declared in this assembly,");
        sb.AppendLine("    ///     discovered at compile time. Registering through these methods avoids the");
        sb.AppendLine("    ///     reflection-based <c>Assembly.GetTypes()</c> scan and keeps trimmed/AOT builds");
        sb.AppendLine("    ///     warning-free: every type is referenced statically via <c>typeof</c>.");
        sb.AppendLine("    /// </summary>");
        sb.Append("    [global::System.CodeDom.Compiler.GeneratedCode(\"Stella.Ergosfare.SourceGenerator\", \"")
          .Append(generatorVersion)
          .AppendLine("\")]");
        sb.AppendLine("    internal static class ErgosfareGeneratedRegistrations");
        sb.AppendLine("    {");

        var wroteMember = false;

        if (builders.HasMessageRegistry)
        {
            sb.AppendLine("        /// <summary>");
            sb.AppendLine("        ///     Registers every Ergosfare construct declared in this assembly into the given");
            sb.AppendLine("        ///     registry, regardless of module.");
            sb.AppendLine("        /// </summary>");
            sb.Append("        public static void RegisterAll(").Append(RegistryFullName).AppendLine(" registry)");
            sb.AppendLine("        {");
            foreach (var type in allTypes)
            {
                sb.Append("            registry.Register(typeof(").Append(type).AppendLine("));");
            }

            sb.AppendLine("        }");
            wroteMember = true;
        }

        if (builders.HasCommandModuleBuilder)
        {
            EmitBuilderExtension(sb, ref wroteMember, CommandBuilderFullName, "command", commandTypes);
        }

        if (builders.HasQueryModuleBuilder)
        {
            EmitBuilderExtension(sb, ref wroteMember, QueryBuilderFullName, "query", queryTypes);
        }

        if (builders.HasEventModuleBuilder)
        {
            EmitBuilderExtension(sb, ref wroteMember, EventBuilderFullName, "event", eventTypes);
        }

        sb.AppendLine("    }");
        sb.AppendLine("}");

        return sb.ToString();
    }

    private static void EmitBuilderExtension(
        StringBuilder sb,
        ref bool wroteMember,
        string builderFullName,
        string moduleName,
        IReadOnlyList<string> types)
    {
        if (wroteMember)
        {
            sb.AppendLine();
        }

        sb.AppendLine("        /// <summary>");
        sb.Append("        ///     Registers every ").Append(moduleName).AppendLine("-module construct declared in this");
        sb.AppendLine("        ///     assembly. Drop-in replacement for <c>RegisterFromAssembly</c>.");
        sb.AppendLine("        /// </summary>");
        sb.Append("        public static ").Append(builderFullName).AppendLine(" RegisterGenerated(");
        sb.Append("            this ").Append(builderFullName).AppendLine(" builder)");
        sb.AppendLine("        {");
        foreach (var type in types)
        {
            sb.Append("            builder.Register(typeof(").Append(type).AppendLine("));");
        }

        sb.AppendLine("            return builder;");
        sb.AppendLine("        }");
        wroteMember = true;
    }
}
